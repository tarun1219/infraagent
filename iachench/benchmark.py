ALL_TASKS: List[BenchmarkTask] = (
    K8S_L1_TASKS + K8S_L2_TASKS + K8S_L3_TASKS + K8S_L4_TASKS + K8S_L5_TASKS +
    TF_L1_TASKS + TF_L2_TASKS + TF_L3_TASKS + TF_L4_TASKS + TF_L5_TASKS +
    DOCKER_L1_TASKS + DOCKER_L2_TASKS + DOCKER_L3_TASKS + DOCKER_L4_TASKS + DOCKER_L5_TASKS
)


def get_tasks_by_language(language: str) -> List[BenchmarkTask]:
    return [t for t in ALL_TASKS if t.language == language]


def get_tasks_by_difficulty(difficulty: int) -> List[BenchmarkTask]:
    return [t for t in ALL_TASKS if t.difficulty == difficulty]


def get_task_by_id(task_id: str) -> BenchmarkTask:
    for task in ALL_TASKS:
        if task.task_id == task_id:
            return task
    raise KeyError(f"Task {task_id} not found")


def summary() -> dict:
    stats = {
        "total": len(ALL_TASKS),
        "by_language": {},
        "by_difficulty": {},
    }
    for lang in ["kubernetes", "terraform", "dockerfile"]:
        stats["by_language"][lang] = len(get_tasks_by_language(lang))
    for diff in range(1, 6):
        stats["by_difficulty"][f"L{diff}"] = len(get_tasks_by_difficulty(diff))
    return stats


class IaCBenchmark:
    """
    High-level interface to the IaCBench benchmark dataset.

    Wraps the module-level task lists and helper functions with a
    class-based API for filtering, sampling, and introspection.
    """

    def get_tasks(
        self,
        task_type: str | None = None,
        difficulty: int | None = None,
        min_difficulty: int | None = None,
        max_difficulty: int | None = None,
        limit: int | None = None,
    ) -> list:
        """
        Return BenchmarkTask objects (with .id, .type, .prompt, .difficulty,
        .validation, .common_failures properties).

        Args:
            task_type:       Filter by language ("kubernetes", "terraform", "dockerfile").
            difficulty:      Exact difficulty level (1–5).
            min_difficulty:  Lower bound (inclusive).
            max_difficulty:  Upper bound (inclusive).
            limit:           Maximum number of tasks to return.
        """
        tasks = list(ALL_TASKS)

        if task_type:
            tasks = [t for t in tasks if t.language == task_type.lower()]
        if difficulty is not None:
            tasks = [t for t in tasks if t.difficulty == difficulty]
        if min_difficulty is not None:
            tasks = [t for t in tasks if t.difficulty >= min_difficulty]
        if max_difficulty is not None:
            tasks = [t for t in tasks if t.difficulty <= max_difficulty]
        if limit is not None:
            tasks = tasks[:limit]

        return tasks

    def get_task_by_id(self, task_id: str) -> BenchmarkTask:
        return get_task_by_id(task_id)

    def summary(self) -> dict:
        return summary()

    def __len__(self) -> int:
        return len(ALL_TASKS)


